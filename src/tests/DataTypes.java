package tests;

public class DataTypes {

	public static void main(String[] args) {

		// int
		int i = 10;
		System.out.println(i);

		i = 20;
		System.out.println(i);
		i = i + 10;
		System.out.println(i);
		i = i * 2;
		System.out.println(i);
		System.out.println(i * 2);
		System.out.println(i);

		System.out.println(i++);
		System.out.println(i);

		System.out.println(++i);
		System.out.println(i);

		// double
		double d = 10.6;
		System.out.println(d);

		d = d * 2;
		System.out.println(d);

		// char
		char a = 'b';
		System.out.println(a);

		char c = '8';
		System.out.println(c);

		// boolean
		boolean b = true;
		System.out.println(b);

		b = false;
		System.out.println(b);
	
		
	}

}
